const createXmasTree = require("./04.js")
const { test, expect } = require("@jest/globals")

const ejemplos = [
  { height: 1, tree: [
    '*',
    '#',
    '#',
  ]},
  { height: 3, tree: [
    '__*__',
    '_***_',
    '*****',
    '__#__',
    '__#__',
  ]},
  { height: 5, tree: [
    '____*____',
    '___***___',
    '__*****__',
    '_*******_',
    '*********',
    '____#____',
    '____#____',
  ]},
  { height: 10, tree: [
    '_________*_________',
    '________***________',
    '_______*****_______',
    '______*******______',
    '_____*********_____',
    '____***********____',
    '___*************___',
    '__***************__',
    '_*****************_',
    '*******************',
    '_________#_________',
    '_________#_________',
  ]},
]

test('Reto04', () => {
  for (let ejemplo of ejemplos) {
    expect(createXmasTree(ejemplo.height)).toBe(ejemplo.tree.join('\n'))
  }
})
